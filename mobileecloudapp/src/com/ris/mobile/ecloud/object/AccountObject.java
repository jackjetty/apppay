package com.ris.mobile.ecloud.object;
import java.io.Serializable;
public class AccountObject  implements Serializable  {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -8982363944431383564L;
	
	
	 
		
		
	private String cardNo;
	private String userName;
	 
	private String cardBalance;
	private String mobileBalance;
	 
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCardBalance() {
		return cardBalance;
	}
	public void setCardBalance(String cardBalance) {
		this.cardBalance = cardBalance;
	}
	public String getMobileBalance() {
		return mobileBalance;
	}
	public void setMobileBalance(String mobileBalance) {
		this.mobileBalance = mobileBalance;
	}
	 
	
	
}